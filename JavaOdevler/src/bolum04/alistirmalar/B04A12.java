package bolum04.alistirmalar;

public class B04A12 {
    public static void main(String[] args) {
        char c1 = 'A';
        int i1 = (int) c1;

        float f = 1000.34f;
        int i2 = (int) f;

        double d = 1000.34;
        int i3 = (int) d;

        int i4 = 97;
        char c2 = (char) i4;

        System.out.println("int i1: " + i1);
        System.out.println("int i2: " + i2);
        System.out.println("int i3: " + i3);
        System.out.println("char c2: " + c2);

    }
}
